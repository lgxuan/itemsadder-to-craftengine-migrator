class LiteralStr(str):
    pass


class Dumper(yaml.SafeDumper):
    pass


def _repr_literal(dumper: yaml.SafeDumper, value: LiteralStr) -> yaml.nodes.ScalarNode:
    return dumper.represent_scalar("tag:yaml.org,2002:str", value, style="|")


Dumper.add_representer(LiteralStr, _repr_literal)


